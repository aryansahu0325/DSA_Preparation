import java.util.Arrays;

public class Fibonacci_DP_N_T
{
    public static void main(String args[])
    {
        int n=5;
        int dp[] = new int[n+1];
        Arrays.fill(dp,-1);
        int result =fibb(n,dp);
        int res =fibbT(n);
        System.out.println(result);
        System.out.println(res);
    }
    private static int fibb(int n, int dp[])
    {
        if(n<=1){
            return n;
        }
        if(dp[n]!=-1)
            return dp[n];
        return dp[n] = fibb(n-1,dp)+fibb(n-2,dp);
    }
    private static int fibbT(int n)
    {
        int []dp=new int[n+1];
        dp[0]=0;
        dp[1]=1;
        for(int i=2;i<=n;i++)
        {
            dp[i]=dp[i-1]+dp[i-2];
        }
        return dp[n];
    }

}
